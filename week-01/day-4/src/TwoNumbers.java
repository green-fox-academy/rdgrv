public class TwoNumbers {
  public static void main(String[] args) {
    int a = 22;
    int b = 13;
    float c = 22f;
    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(c / b);
    System.out.println(a / b);
    System.out.println(a % b);

  }
}
